function multi(num1, num2) {
    return num1 * num2;
}

function sum(num1, num2) {
    return num1 + num2;
}

function menos(num1, num2) {
    return num1 - num2;
}

function div(num1, num2) {
    return num1 / num2
}

console.log(multi(2, 3))
console.log(sum(2, 8))
console.log(menos(4, 7))
console.log(div(20, 10))