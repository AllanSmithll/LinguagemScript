//WHILE é uma estrutura de repetição lida como "Enquanto". Ela é usada principalmente quando não sabemos o intervalo de repetição desejado. Inclusive, é usado também como "loop infinito"

let count = 1;

while (count <= 10) {
    console.log(count)
    count += 1
}

let infinito = true;

while (infinito == true) {
    console.log("Loop infinito")
}